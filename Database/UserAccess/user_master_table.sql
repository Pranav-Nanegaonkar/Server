-- =====================================================
-- TABLE: user_master
-- =====================================================
CREATE TABLE `user_master` (
    `ID` INT UNSIGNED NOT NULL AUTO_INCREMENT,
    `NAME` VARCHAR(100) NOT NULL,
    `EMAIL_ID` VARCHAR(150) NOT NULL,
    `MOBILE_NUMBER` VARCHAR(20) NOT NULL,
    `PASSWORD` VARCHAR(255) NOT NULL,
    `CLIENT_ID` INT DEFAULT NULL,
    `IS_ACTIVE` TINYINT(1) NOT NULL DEFAULT 1,
    `CREATED_DATE` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    `CREATED_MODIFIED_DATE` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP 
        ON UPDATE CURRENT_TIMESTAMP,

    PRIMARY KEY (`ID`),

    UNIQUE KEY `uk_user_email` (`EMAIL_ID`),
    UNIQUE KEY `uk_user_mobile` (`MOBILE_NUMBER`),

    INDEX `idx_user_active` (`IS_ACTIVE`),
    INDEX `idx_user_client` (`CLIENT_ID`),
    INDEX `idx_user_login` (`MOBILE_NUMBER`, `EMAIL_ID`, `IS_ACTIVE`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- =====================================================
-- VIEW: view_user_master
-- Used for SELECT, LOGIN, PAGINATION
-- =====================================================
CREATE OR REPLACE VIEW `view_user_master` AS
SELECT
    ID,
    NAME,
    EMAIL_ID,
    MOBILE_NUMBER,
    PASSWORD,
    CLIENT_ID,
    IS_ACTIVE,
    CREATED_DATE,
    CREATED_MODIFIED_DATE
FROM user_master;

